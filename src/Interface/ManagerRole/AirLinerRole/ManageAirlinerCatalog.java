    private void DeleteScheduleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteScheduleBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = AirlinerCatalog.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                AirlinerSchedule sched = (AirlinerSchedule)AirlinerCatalog.getValueAt(selectedRow, 0);
                airlinerschedule.getAirlineScheduleDirectory().removeAirlinerSchedule(sched);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
        
        
        
    }//GEN-LAST:event_DeleteScheduleBtnActionPerformed


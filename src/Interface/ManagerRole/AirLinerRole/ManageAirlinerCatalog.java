 public void populate(){
        DefaultTableModel dtm = (DefaultTableModel)AirlinerCatalog.getModel();
        dtm.setRowCount(0);
        
        for(AirlinerSchedule B : airlinerschedule.getAirlineScheduleDirectory().getAirlinerScheduleDirect()){
            Object[] row = new Object[3];
            row[0]=B;
            row[1]=B.getAirline();
            row[2]=B.getFlightNumber();
            
            dtm.addRow(row);
        }
    }
    
